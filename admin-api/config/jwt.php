return [

    /*
    |--------------------------------------------------------------------------
    | JWT認証シークレット
    |--------------------------------------------------------------------------
    |
    | .envファイルで設定してください。トークンの署名に使用されます。
    | ヘルパーコマンド: `php artisan jwt:secret`
    |
    | 注意: 対称アルゴリズム（HMAC）でのみ使用されます。
    | RSAやECDSAは公開鍵/秘密鍵のペアを使用します（下記参照）。
    |
    */

    'secret' => env('JWT_SECRET'),

    /*
    |--------------------------------------------------------------------------
    | JWT認証キー
    |--------------------------------------------------------------------------
    |
    | 使用するアルゴリズムによって、トークンがランダム文字列
    | （`JWT_SECRET`で定義）で署名されるか、以下の公開鍵/秘密鍵で
    | 署名されるかが決まります。
    |
    | 対称アルゴリズム:
    | HS256, HS384, HS512 は `JWT_SECRET` を使用します。
    |
    | 非対称アルゴリズム:
    | RS256, RS384, RS512 / ES256, ES384, ES512 は以下のキーを使用します。
    |
    */

    'keys' => [

        /*
        |--------------------------------------------------------------------------
        | 公開鍵
        |--------------------------------------------------------------------------
        |
        | 公開鍵のパスまたはリソースを指定します。
        |
        */

        'public' => env('JWT_PUBLIC_KEY'),

        /*
        |--------------------------------------------------------------------------
        | 秘密鍵
        |--------------------------------------------------------------------------
        |
        | 秘密鍵のパスまたはリソースを指定します。
        |
        */

        'private' => env('JWT_PRIVATE_KEY'),

        /*
        |--------------------------------------------------------------------------
        | パスフレーズ
        |--------------------------------------------------------------------------
        |
        | 秘密鍵のパスフレーズです。設定がなければnullにできます。
        |
        */

        'passphrase' => env('JWT_PASSPHRASE'),

    ],

    /*
    |--------------------------------------------------------------------------
    | JWTの有効期間
    |--------------------------------------------------------------------------
    |
    | トークンが有効な期間（分）を指定します。
    | デフォルトは1時間です。
    |
    | nullに設定すると、有効期限のないトークンになります。
    | モバイルアプリなどで使いたい場合がありますが、推奨されません。
    | 必要に応じてトークンを無効化する仕組みを用意してください。
    | 注意: nullに設定する場合、'required_claims'から'exp'を削除してください。
    |
    */

    'ttl' => env('JWT_TTL', 60),

    /*
    |--------------------------------------------------------------------------
    | リフレッシュの有効期間
    |--------------------------------------------------------------------------
    |
    | トークンをリフレッシュできる期間（分）を指定します。
    | ユーザーは元のトークン作成から2週間以内にリフレッシュでき、
    | それを過ぎると再認証が必要です。デフォルトは2週間です。
    |
    | nullに設定すると、無限のリフレッシュ期間になります。
    | 有効期限のないトークンの代わりにモバイルアプリなどで使えますが、
    | 推奨されません。トークンを無効化する仕組みを用意してください。
    |
    */

    'refresh_ttl' => env('JWT_REFRESH_TTL', 20160),

    /*
    |--------------------------------------------------------------------------
    | JWTハッシュアルゴリズム
    |--------------------------------------------------------------------------
    |
    | トークンの署名に使用するハッシュアルゴリズムを指定します。
    |
    | 利用可能な値: https://github.com/namshi/jose/tree/master/src/Namshi/JOSE/Signer/OpenSSL
    |
    */

    'algo' => env('JWT_ALGO', 'HS256'),

    /*
    |--------------------------------------------------------------------------
    | 必須クレーム
    |--------------------------------------------------------------------------
    |
    | トークンに必ず含まれるべきクレームを指定します。
    | これらのクレームがペイロードに含まれていない場合、
    | TokenInvalidExceptionがスローされます。
    |
    */

    'required_claims' => [
        'iss',
        'iat',
        'exp',
        'nbf',
        'sub',
        'jti',
    ],

    /*
    |--------------------------------------------------------------------------
    | 永続クレーム
    |--------------------------------------------------------------------------
    |
    | トークンをリフレッシュする際に保持するクレームキーを指定します。
    | `sub`と`iat`は自動的に保持されます。
    |
    | 注意: 存在しないクレームは無視されます。
    |
    */

    'persistent_claims' => [
        'type',
        'role',
        'name',
        'email',
    ],

    /*
    |--------------------------------------------------------------------------
    | サブジェクトロック
    |--------------------------------------------------------------------------
    |
    | `prv`クレームを自動的にトークンに追加するかどうかを決定します。
    | 複数の認証モデル（例: `App\User`と`App\OtherPerson`）がある場合、
    | ある認証リクエストが別のモデルになりすますことを防ぎます。
    |
    | 認証モデルが1つだけの場合、この機能を無効にすることで
    | トークンサイズを少し削減できます。
    |
    */

    'lock_subject' => true,

    /*
    |--------------------------------------------------------------------------
    | 許容誤差（リーウェイ）
    |--------------------------------------------------------------------------
    |
    | JWTのタイムスタンプクレームに「許容誤差」を設定します。
    | サーバー間でわずかな時刻のずれがある場合に
    | ある程度の余裕を持たせることができます。
    |
    | `iat`、`nbf`、`exp`クレームに適用されます。
    |
    | 秒単位で指定 - 必要な場合のみ設定してください。
    |
    */

    'leeway' => env('JWT_LEEWAY', 0),

    /*
    |--------------------------------------------------------------------------
    | ブラックリストの有効化
    |--------------------------------------------------------------------------
    |
    | トークンを無効化するには、ブラックリストを有効にする必要があります。
    | この機能が不要な場合はfalseに設定してください。
    |
    */

    'blacklist_enabled' => env('JWT_BLACKLIST_ENABLED', true),

    /*
    | -------------------------------------------------------------------------
    | ブラックリストの猶予期間
    | -------------------------------------------------------------------------
    |
    | 同じJWTで複数の同時リクエストが行われた場合、
    | リクエストごとのトークン再生成により一部が失敗する可能性があります。
    |
    | この猶予期間は、トークンがブラックリストに追加された後も
    | 一定時間使用を許可します。
    |
    | できるだけ短く設定してください。
    |
    */

    'blacklist_grace_period' => env('JWT_BLACKLIST_GRACE_PERIOD', 0),

    /*
    |--------------------------------------------------------------------------
    | Cookieの暗号化
    |--------------------------------------------------------------------------
    |
    | Laravelはセキュリティのためデフォルトでcookieを暗号化します。
    | cookieを復号しない場合、LaravelのEncryptCookiesミドルウェアの
    | $except配列にcookieトークン名を追加する必要があります。
    | 詳細: https://laravel.com/docs/master/responses#cookies-and-encryption
    |
    | cookieを復号する場合はtrueに設定してください。
    |
    */

    'decrypt_cookies' => false,

    /*
    |--------------------------------------------------------------------------
    | プロバイダー
    |--------------------------------------------------------------------------
    |
    | パッケージ全体で使用する各種プロバイダーを指定します。
    |
    */

    'providers' => [

        /*
        |--------------------------------------------------------------------------
        | JWTプロバイダー
        |--------------------------------------------------------------------------
        |
        | トークンの作成とデコードに使用するプロバイダーを指定します。
        |
        */

        'jwt' => Tymon\JWTAuth\Providers\JWT\Lcobucci::class,

        /*
        |--------------------------------------------------------------------------
        | 認証プロバイダー
        |--------------------------------------------------------------------------
        |
        | ユーザーの認証に使用するプロバイダーを指定します。
        |
        */

        'auth' => Tymon\JWTAuth\Providers\Auth\Illuminate::class,

        /*
        |--------------------------------------------------------------------------
        | ストレージプロバイダー
        |--------------------------------------------------------------------------
        |
        | ブラックリストにトークンを保存するプロバイダーを指定します。
        |
        */

        'storage' => Tymon\JWTAuth\Providers\Storage\Illuminate::class,

    ],

];